package main

import (
	"fmt"
)

func isValidSudoku(board [][]byte) bool {
	rows := [9][9]bool{}
	cols := [9][9]bool{}
	squrs := [9][9]bool{}
	for row := 0; row < len(board); row++ {
		for col := 0; col < len(board[row]); col++ {
			cell := board[row][col]
			if cell == '.' {
				continue
			}

			cell = cell - '0' - 1
			squr := (row/3)*3 + col/3 // 將9x9映射成3x3小方格，每個小方格為3x3
			if rows[row][cell] || cols[col][cell] || squrs[squr][cell] {
				return false
			}
			rows[row][cell] = true
			cols[col][cell] = true
			squrs[squr][cell] = true
		}
	}

	return true
}

